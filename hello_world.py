# Numeric literals

# Numeric Literals are immutable (unchangeable). Numeric literals can belong to 3 different numerical types: Integer,
# Float, and Complex.

# 1. Integer Literals Integer literals are numbers without decimal parts. It also consists of negative numbers. For
# example, 5, -11, 0, 12, etc.
#
# 2. Floating-Point Literals
# Floating-point literals are numbers that contain decimal parts.
#
# Just like integers, floating-point numbers can also be both positive and negative. For example, 2.5, 6.76, 0.0,
# -9.45, etc.
#
# 3. Complex Literals
# Complex literals are numbers that represent complex numbers.
#
# Here, numerals are in the form a + bj, where a is real and b is imaginary. For example, 6+9j, 2+3j.

